    // 매개변수 2개를 받고 , 매개변수 2개를 더한값을 리턴 하는
    // 'sum' 이라는 이름으로 함수 정의하고
    // 'sum' 함수을 실행할때 10 , 5 매개변수로 전달하고 결과를 출력하시오.

// 1. 함수 정의/만들기 = 만들때 사용되는 키워드 = function 함수명( ){ }
    // 함수명 과 매개변수명은 임의로 
function sum( 매개변수1 , 매개변수2  ){
    let 더한값 = 매개변수1 + 매개변수2; // 함수가 처리하는 로직/코드
    return 더한값; // 함수가 종료될때 함수를 호출했던 곳을 리턴 값을 반환
} // f end 

// 2. 함수 호출 = 함수명(); = 리턴값을 있을경우 변수에 저장 
let sum결과 = sum( 10 , 5 );
document.write(`<p> sum함수의 결과 : ${ sum결과 } </p>`)

// 3. 함수 또 호출 하기 ( 재활용/사용 )
