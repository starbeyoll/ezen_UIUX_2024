// 활용2 )  구구단 함수 
    // 매개변수에 1개( 단 ) 받고 , 해당 단의 구구단을 출력하시오. 
    // 'gugudan' 이라는 이름으로 함수 정의하고
    // 'gugudan' 함수을 실행할때 3 매개변수로 전달하고 실행하시오.
    // 예) gugudan(5)  실행했을때 5단 구구단 출력.

// 1. 함수 정의
function gugudan( 단 ) {
    // 매개변수의 구구단 반복하기 
    for( let 곱 = 1 ; 곱<=9 ; 곱++ ){
        document.write( `<p> ${ 단 } X ${ 곱 } = ${ 단*곱 }  </p>` )
    } // for end 
    return; // 리턴값이 없다.// 생략가능.
}// f end 

// 2. 함수 호출 
gugudan( 3 );   // 3단
gugudan( 4 );   // 4단
gugudan( 5 );   // 5단 

// 3. 단 입력받아.. 구구단 함수에 매개변수로 전달
let dan = prompt('단 입력해주세요');
gugudan( dan );